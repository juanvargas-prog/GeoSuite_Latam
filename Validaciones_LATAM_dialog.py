    def cargar_capas_filtradas(self):
        nombres_deseados = [
            'mavvial', 'placa', 'manzana', 'concentracion urbana', 'distrito',
            'estado', 'centro_poblado', 'parque_y_zona_verde', 'hidrografia', 'predio', 'cep'
        ]

        # Obtener todas las capas del proyecto actual
        capas_proyecto = QgsProject.instance().mapLayers().values()

        # Filtrar capas por nombre
        capas_filtradas = [capa for capa in capas_proyecto if capa.name().lower() in nombres_deseados]

        # Asignar capas filtradas a los combos val1 a val12
        for i, capa in enumerate(capas_filtradas[:12]):  # Máximo 12 combos
            combo = getattr(self, f'val{i+1}', None)
            if combo:
                combo.setLayer(capa)

