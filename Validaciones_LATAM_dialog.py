    def credenciales(self, db):
        # Obtiene los parámetros de conexión desde la configuración
        settings = QgsSettings()
        dbname = settings.value('/PostgreSQL/connections/' + db + '/database', '')
        host = settings.value('/PostgreSQL/connections/' + db + '/host', '')
        port = settings.value('/PostgreSQL/connections/' + db + '/port', '')
        user = settings.value('/PostgreSQL/connections/' + db + '/username', '')
        password = settings.value('/PostgreSQL/connections/' + db + '/password', '')

        # Si los parámetros de conexión están vacíos, es posible que desees manejar esta situación
        if not dbname or not host or not port or not user or not password:
            print("Faltan parámetros de conexión.")
            #self.message('No se ejecutó', 'Faltan parámetros de conexión.')
            
        return dbname, host, port, user, password

