            self.mensaje('Procesando', '☁️ Autenticando con Google Drive...')
            self.mostrarProceso.setText(
                self.mostrarProceso.toPlainText() + '\n☁️ Conectando con Google Drive...')
            QApplication.processEvents()

            service = build_service(CLIENT_SECRET_PATH, TOKEN_PATH)

            self.mensaje('Procesando', '☁️ Subiendo carpeta a Google Drive...')
            self.mostrarProceso.setText(
                self.mostrarProceso.toPlainText() + '\n☁️ Subiendo archivos...')
            QApplication.processEvents()

            folder_id = upload_folder(service, LOCAL_FOLDER, drive_folder_id, bd)

