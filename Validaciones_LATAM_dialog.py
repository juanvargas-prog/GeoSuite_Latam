    def obtener_columna_pk_desde_tabla(self, engine, tabla):
        """Obtiene el nombre de la columna de clave primaria"""
        try:
            tabla_simple = tabla.split('.')[-1].strip('"')
            query = f"""
                SELECT column_name
                FROM information_schema.key_column_usage kcu
                JOIN information_schema.table_constraints tc 
                ON kcu.constraint_name = tc.constraint_name
                WHERE tc.table_name = '{tabla_simple}' 
                AND tc.constraint_type = 'PRIMARY KEY'
                LIMIT 1
            """
            df = pd.read_sql(query, engine)
            return df['column_name'].iloc[0] if not df.empty else 'id'
        except:
            return 'id'
                
