    def validar_jerarquia_administrativa(self, engine, capas_administrativas, resumen_texto, total_errores):
        """Valida la jerarquía correcta entre capas administrativas"""
        jerarquia = ['PAIS', 'ESTADO/DEPARTAMENTO/PROVINCIA', 'MUNICIPIO/COMUNA/CANTON/ALCALDIAS', 'MANZANA']
        
        capas_disponibles = {}
        for nivel in jerarquia:
            for nombre, capa in capas_administrativas.items():
                if nivel in nombre and capa:
                    capas_disponibles[nivel] = capa
                    break
        
        # Validar contención jerárquica
        niveles = list(capas_disponibles.keys())
        
        for i in range(len(niveles) - 1):
            nivel_superior = niveles[i]
            nivel_inferior = niveles[i + 1]
            
            tabla_sup = self.obtener_nombre_tabla_desde_capa(capas_disponibles[nivel_superior])
            tabla_inf = self.obtener_nombre_tabla_desde_capa(capas_disponibles[nivel_inferior])
            
            geom_sup = self.obtener_columna_geometria_desde_capa(capas_disponibles[nivel_superior])
            geom_inf = self.obtener_columna_geometria_desde_capa(capas_disponibles[nivel_inferior])
            
            query = f"""
            SELECT COUNT(*) as errores_jerarquia
            FROM {tabla_inf} inf
            WHERE NOT EXISTS (
                SELECT 1 FROM {tabla_sup} sup
                WHERE sup.{geom_sup} && inf.{geom_inf}
                AND ST_Within(inf.{geom_inf}, sup.{geom_sup})
            )
            """
            
            try:
                errores = self.run3(query)[0][0]
                #print(f"   📊 Jerarquía {nivel_inferior} → {nivel_superior}: {errores} errores")
                resumen_texto += f"     📊 Jerarquía {nivel_inferior} → {nivel_superior}: {errores} errores \n"
                
                if errores != 0:
                    total_errores += 1
                
            except Exception as e:
                print(f"   ⚠️ Error validando jerarquía {nivel_inferior} → {nivel_superior}: {e}")
                resumen_texto += f"     ⚠️ Error validando jerarquía {nivel_inferior} → {nivel_superior}: {e}\n"
        
        return resumen_texto, total_errores
        
        
